''' 113. Faça um algoritmo que converta metros para centímetros:

converter metros para centímetros
receber o tamanho em metros
multiplicar o tamanho por 100
mostrar o resultado da multiplicação.'''

metros = int(input('Informe o valor em metros: '))

centimetros = (metros * 100)

print('{0} metros em centímetros é {1}'.format(metros, centimetros))
